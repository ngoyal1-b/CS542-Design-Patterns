package coursesRegistration.util;

public class CourseInfo {
		String course;
		int capacity;
		String timings;
		
		public String getCourse() {
			return course;
		}
		public void setCourse(String courseIN) {
			course = courseIN;
		}
		public int getCapacity() {
			return capacity;
		}
		public void setCapacity(int capacityIN) {
			capacity = capacityIN;
		}
		public String getTimings() {
			return timings;
		}
		public void setTimings(String timingsIN) {
			timings = timingsIN;
		}
		
		
}
